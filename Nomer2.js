const readline = require("readline");
const r1 = readline.createInterface(
    {
        input : process.stdin,
        output : process.stdout
    }
)

const input = (Question)=> {
    return new Promise(function(resolve,reject) {
        r1.question(Question, (answer1) => {
            try {
                resolve(answer1);
            }catch(err) {
                reject(err)
            }
        })
    })
}

const main = async () => {

    let leaderboard = await input ("masuka leaderboard : ")
    const arrLeader = leaderboard.split(", ")

    let player = await input ("masuka player : ")
    const arrPlayer = player.split(", ")
    .map((v, i) => [v, i])
    .sort((a, b) => b[0] - a[0])
    .map((a, i) => [...a, i + 1])
    .sort((a, b) => a[1] - b[1])
    .map(a => a[2]);


    console.log(arrPlayer)

    process.exit();
}

main()